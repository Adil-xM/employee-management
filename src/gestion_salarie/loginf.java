    private void LoginbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginbuttonActionPerformed
            PreparedStatement prs;
            ResultSet rst;
            String username = usernameField.getText();
            String mdp = String.valueOf(PasswordField.getPassword());
            
            if (username.trim().equals("") || mdp.trim().equals(""))
            {
                JOptionPane.showMessageDialog(rootPane, "un ou les deux champs sont vides", "attention", 2);
            }
            else {
                loginPerformed connect=new loginPerformed();
                String query="SELECT * FROM `utilisateurs` WHERE `username`=? AND `mot_de_passe`=?";
                
                try {
                    prs = connect.connectdb().prepareStatement(query);
                    prs.setString(1, username);
                    prs.setString(2, mdp);
                    
                    rst=prs.executeQuery();
                    
                    if (rst.next()){
                        page_initiale pginit=new page_initiale();
                        pginit.setVisible(true);
                        pginit.pack();
                        pginit.setLocationRelativeTo(null);
                        
                       this.dispose();
                       
                    }
                    else {
                        JOptionPane.showMessageDialog(rootPane, "votre nom d'utilisateur ou mot de passe est incorrectes","Erreur!!",2);
                        
                        
                    }
                
                } catch (SQLException ex) {
                    Logger.getLogger(loginf.class.getName()).log(Level.SEVERE, null, ex);
                }
               
            }
            
            
           
    }//GEN-LAST:event_LoginbuttonActionPerformed

