    private void AjouterbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AjouterbuttonActionPerformed
         String Prenom=PrenomField.getText();
         String Nom=NomField.getText();
         String Genre=genre;
         String CIN=CINField.getText();
         String Adresse=AdresseField.getText();
         String NTelephone=NTelephoneField.getText();
         String DateEmbauche ="";
        if (jDateChooser1.getDate()!= null){
            SimpleDateFormat date=new SimpleDateFormat("yyyy-MM-dd");
            DateEmbauche=date.format(jDateChooser1.getDate());
        }
         
         String Email=EmailField.getText();
         String Fonction=FonctionField.getText();
        
         if(NTelephone.equals("")|| Prenom.equals("")|| Nom.equals("")|| Genre.equals("")|| CIN.equals("") || Adresse.equals("")||DateEmbauche.equals("") ||Fonction.equals(""))
         {
             JOptionPane.showMessageDialog(rootPane, "un ou des champs importantes sont vides","Erreur!!",2);
         }
         else {
             Connectiondb connect=new Connectiondb();
             PreparedStatement prs;
             ResultSet rst;
             String query="INSERT INTO `salarie` (`prenom`, `nom`, `genre`, `CIN`, `adresse`, `n_telephone`,`date_embauche`, `email`, `fonction`) VALUES (?,?,?,?,?,?,?,?,?)";
             try {
                 prs=connect.connectdb().prepareStatement(query);
                 prs.setString(1, Prenom);
                 prs.setString(2, Nom);
                 prs.setString(3, Genre);
                 prs.setString(4, CIN);
                 prs.setString(5, Adresse);
                 prs.setString(6, NTelephone);
                 prs.setString(7, DateEmbauche);
                 prs.setString(8, Email);
                 prs.setString(9, Fonction);
                 
                 
                 if(prs.executeUpdate() > 0){
                     JOptionPane.showMessageDialog(rootPane, "Salarié Ajouter","succés",JOptionPane.INFORMATION_MESSAGE);
                 }
                 else{
                     JOptionPane.showMessageDialog(rootPane, "Erreur salarié non Ajouter","Erreur!!",2);
                 }
                 
             } catch (SQLException ex) {
                 Logger.getLogger(ModificationSalarie.class.getName()).log(Level.SEVERE, null, ex);
                 JOptionPane.showMessageDialog(rootPane, "Erreur salarié non Ajouter","Erreur!!",2);
             }
             
         }
         
    }//GEN-LAST:event_AjouterbuttonActionPerformed

