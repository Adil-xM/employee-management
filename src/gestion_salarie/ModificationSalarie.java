    public void AFFToustable(JTable table){
        PreparedStatement prs;
        ResultSet rst;
        Connectiondb connect=new Connectiondb();
        
        String query="SELECT * FROM `salarie`";
        try {
            prs=connect.connectdb().prepareStatement(query);
            rst=prs.executeQuery();
            DefaultTableModel tableMd=(DefaultTableModel)table.getModel();
            Object[] row;

            while(rst.next()){
                row=new Object[10];
                row[0]=rst.getInt(1);
                row[1]=rst.getString(3);
                row[2]=rst.getString(4);
                row[3]=rst.getString(5);
                row[4]=rst.getString(6);
                row[5]=rst.getString(7);
                row[6]=rst.getString(8);
                row[7]=rst.getDate(9);
                row[8]=rst.getString(10);
                row[9]=rst.getString(11);
                
                tableMd.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ModificationSalarie.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
              
