    private void ajouterbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterbuttonActionPerformed
        PreparedStatement prs;
        ResultSet rst;
        Connectiondb connect=new Connectiondb();
        String nImmatriculation=N_immatriculationfield.getText();
        String type=TypeField.getSelectedItem().toString();
        String marque=marqueField.getText();
        String modele=modeleField.getText();
        String couleur=couleurField.getText();
        //String IDutilisateur=IDutilisateurField.getText();
        
        String query="INSERT INTO `vehicule`(`n_imatriculation`, `type`, `marque`, `modéle`, `couleur`) VALUES (?,?,?,?,?)";
        if(nImmatriculation.equals("") || type.equals("" ) || marque.equals("") || modele.equals("") || couleur.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "un ou des champs importantes sont vides !!","Erreur",JOptionPane.WARNING_MESSAGE);
            
        }
        else {
            try {
            prs=connect.connectdb().prepareStatement(query);
            prs.setString(1, nImmatriculation);
            prs.setString(2, type);
            prs.setString(3, marque);
            prs.setString(4, modele);
            prs.setString(5, couleur);
            prs.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, type+" est ajouter", "succés", JOptionPane.INFORMATION_MESSAGE);
          } catch (SQLException ex) {
            Logger.getLogger(EtatVehicule.class.getName()).log(Level.SEVERE, null, ex);
          }
        }
        
    }//GEN-LAST:event_ajouterbuttonActionPerformed

    private void historiquebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_historiquebuttonActionPerformed
        PreparedStatement prs;
        ResultSet rst;
        Connectiondb connect=new Connectiondb();
        String nImmatriculation=N_immatriculationfield.getText();
        
        String query="SELECT N_immatriculation,type,marque,modéle,couleur,prenom,nom,date.date_debut FROM vehicule_utilisation,vehicule,salarie,date WHERE salarie.ID_salarie=vehicule_utilisation.ID_salarie AND vehicule_utilisation.N_immatriculation=vehicule.n_imatriculation AND date.ID_Date=vehicule_utilisation.ID_date AND N_immatriculation=?;";
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
         try {
            prs=connect.connectdb().prepareStatement(query);
            prs.setString(1, nImmatriculation);
            rst=prs.executeQuery();
            DefaultTableModel tableMd=(DefaultTableModel)jTable1.getModel();
            Object[] row;

            while(rst.next()){
                row=new Object[7];
                row[0]=rst.getInt(1);
                row[1]=rst.getString(2);
                row[2]=rst.getString(3);
                row[3]=rst.getString(4);
                row[4]=rst.getString(5);
                row[5]=rst.getString(6)+" "+rst.getString(7);
                row[6]=rst.getDate(8);
                tableMd.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ModificationSalarie.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_historiquebuttonActionPerformed

