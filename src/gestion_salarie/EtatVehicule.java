    private void modifierUtilisateurbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierUtilisateurbuttonActionPerformed
        PreparedStatement prs;
        ResultSet rst;
        Connectiondb connect=new Connectiondb();
        String nImmatriculation=N_immatriculationfield.getText();
        String IDutilisateur=IDutilisateurField.getText();
        String date_util="";
        
        String query="INSERT INTO `vehicule_utilisation` (`ID_salarie`, `N_immatriculation`, `ID_date`) VALUES (?,?,?);";
        
        try {
            if(date_utilisation.getDate()!=null && !"".equals(IDutilisateur)){
           SimpleDateFormat date=new SimpleDateFormat("yyyy-MM-dd");
           date_util=date.format(date_utilisation.getDate());
           String query2="INSERT INTO `date` (`date_debut`) VALUES (?)";
           prs=connect.connectdb().prepareStatement(query2);
           prs.setString(1, date_util);
           prs.executeUpdate();
           
           String query4="SELECT ID_Date FROM date WHERE date_debut=?";
           prs=connect.connectdb().prepareStatement(query4);
           prs.setString(1, date_util);
           rst=prs.executeQuery();
           String id_date="";
            if(rst.next())
             {
                 id_date=rst.getString(1);
             }
            
            prs=connect.connectdb().prepareStatement(query);
            prs.setString(1, IDutilisateur);
            prs.setString(2, nImmatriculation);
            prs.setString(3, id_date);
            if(prs.executeUpdate()>0)
            {
                JOptionPane.showMessageDialog(rootPane, "Modification appliquer","succés",JOptionPane.INFORMATION_MESSAGE);
            }
            else {
                JOptionPane.showMessageDialog(rootPane, "Erruer vehicule ou salarie n'exist pas !!!!","Erreur",JOptionPane.WARNING_MESSAGE);
            }
         }
            else{
                JOptionPane.showMessageDialog(rootPane, "vous devez remplir la date et ID d'utilisateur !!!","Erreur",JOptionPane.WARNING_MESSAGE);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(EtatVehicule.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Erruer vehicule ou salarie n'exist pas !!!!","Erreur",JOptionPane.WARNING_MESSAGE);
        }
        
        
        
        
    }//GEN-LAST:event_modifierUtilisateurbuttonActionPerformed

